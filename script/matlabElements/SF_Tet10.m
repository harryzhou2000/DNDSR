function N = SF_Tet10(xi,et,zt)
a = 1 - xi - et - zt;
b = xi;
c = et;
d = zt;
N = [...
   (a - 0) * (a - 0.5) / (1 - 0) / (1 - 0.5)
   (b - 0) * (b - 0.5) / (1 - 0) / (1 - 0.5)
   (c - 0) * (c - 0.5) / (1 - 0) / (1 - 0.5)
   (d - 0) * (d - 0.5) / (1 - 0) / (1 - 0.5)
   a / 0.5 * b / 0.5;
   b / 0.5 * c / 0.5;
   c / 0.5 * a / 0.5;
   a / 0.5 * d / 0.5;
   b / 0.5 * d / 0.5;
   c / 0.5 * d / 0.5;
]';

end